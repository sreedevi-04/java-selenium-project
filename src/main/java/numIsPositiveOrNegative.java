public class numIsPositiveOrNegative {
     public static void main(String[]args){
          int num=-63;
          if(num>0){
               System.out.print(num+ " is a positive number");
          }
          else if(num<0) {
               System.out.print(num+ " is a negative number");
          }
          else{
               System.out.print(num+ "it's a zero");
          }

     }
}
