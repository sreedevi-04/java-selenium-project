public class doWhileLoop {
    public static void main(String[] args) {
        //if the condition is not sastified the statement will execute atleast once in do while loop
        //where as in while loop, if the condition is not sastified the statement will not execute even for once
        int i=10;
        do{
            System.out.println(i);
            i++;
        }
        while(i<=5);
    }
}
